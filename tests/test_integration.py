def test_web_flow_with_fake_services(tmp_path, fpois, by_id):
    state_store = StateStore(tmp_path / "state.json")
    nominatim = FakeNominatim(results=fpois)
    osrm = FakeOsrm()
    # Pre-register routes that will be needed
    hub = by_id["node:1000"]
    book = by_id["node:1003"]
    osrm.register(book, hub, duration_s=180.0)

    # Board is now seeded at setup time (Overpass), not via gameplay search.
    # Direct-seed state_store with fpois — matches the post-setup invariant
    # without coupling this test to OVERPASS_MIN_POIS thresholds.
    seeded = new_game()
    seeded.merge_discovered_pois(fpois)
    state_store.save(seeded)

    app = create_app(
        config=Config(),
        state_store=state_store,
        nominatim_client=nominatim,
        osrm_client=osrm,
    )
    app.config["TESTING"] = True
    client = app.test_client()

    saved = state_store.load()
    assert len(saved.pois) == len(fpois)

    # POST /move (P1 first, free)
    resp = client.post("/move", data={"poi_id": hub.id})
    assert resp.status_code == 302
    assert state_store.load().turn_index == 1
    assert state_store.load().get_poi(hub.id).owner == 1

    # POST /move P2 first flag (far)
    far = by_id["node:1009"]
    resp = client.post("/move", data={"poi_id": far.id})
    assert resp.status_code == 302
    assert state_store.load().turn_index == 2

    # POST /move P1 second flag — book, route to hub
    resp = client.post("/move", data={"poi_id": book.id})
    assert resp.status_code == 302
    saved = state_store.load()
    assert saved.turn_index == 3
    assert saved.get_poi(book.id).owner == 1
    assert len(saved.routes) == 1

    # GET /map produces Folium HTML containing polyline + POI name
    resp = client.get("/map")
    assert resp.status_code == 200
    assert b"leaflet" in resp.data.lower()
    assert b"polyline" in resp.data.lower()
    assert hub.name.encode() in resp.data

    # GET /api/state returns JSON
    resp = client.get("/api/state")
    assert resp.status_code == 200
    data = resp.get_json()
    assert data["turn_index"] == 3
    assert data["status"] == "active"


