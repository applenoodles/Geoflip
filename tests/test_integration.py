def _make_extra_poi(poi_id: str, east_m: float, base_lat=25.0330, base_lon=121.5654):
    """Generate a POI east_m metres east of base via pyproj."""
    from app.models import Poi
    to_m, to_wgs = build_meter_transformers(base_lon, base_lat)
    bx, by = to_m.transform(base_lon, base_lat)
    lon, lat = to_wgs.transform(bx + east_m, by)
    return Poi(
        id=poi_id, name=f"Extra {poi_id}", lat=lat, lon=lon,
        osm_type="node", osm_id=hash(poi_id) & 0xFFFF,
        category="amenity", poi_type="cafe", score=2,
        owner=None, discovered_turn=0, placed_turn=None, raw={},
    )


