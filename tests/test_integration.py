def test_web_invalid_move_does_not_persist(tmp_path, fpois, by_id):
    state_store = StateStore(tmp_path / "state.json")
    pre = new_game()
    pre.pois = list(fpois)
    state_store.save(pre)
    pre_loaded = state_store.load()

    app = create_app(
        config=Config(),
        state_store=state_store,
        nominatim_client=FakeNominatim(),
        osrm_client=FakeOsrm(),
    )
    app.config["TESTING"] = True
    client = app.test_client()

    # Move on a POI already owned by P2
    p = by_id["node:1000"]
    p.owner = 2
    saved = state_store.load()
    saved.get_poi(p.id).owner = 2
    state_store.save(saved)
    pre2 = state_store.load()

    resp = client.post("/move", data={"poi_id": p.id})
    assert resp.status_code == 302
    post = state_store.load()
    assert post.turn_index == pre2.turn_index
    assert post.get_poi(p.id).owner == 2
